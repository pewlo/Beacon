extension UIColor {
    @nonobjc class var bluePurple: UIColor {
        return UIColor(red: 61.0 / 255.0, green: 14.0 / 255.0, blue: 238.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var lightBluePurple: UIColor {
        return UIColor(red: 178.0 / 255.0, green: 162.0 / 255.0, blue: 243.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var ultraLightBluePurple: UIColor {
        return UIColor(red: 226.0 / 255.0, green: 219.0 / 255.0, blue: 252.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var ultraLightGrey: UIColor {
        return UIColor(red: 245.0 / 255.0, green: 245.0 / 255.0, blue: 245.0 / 255.0, alpha: 1.0)
    }
}

